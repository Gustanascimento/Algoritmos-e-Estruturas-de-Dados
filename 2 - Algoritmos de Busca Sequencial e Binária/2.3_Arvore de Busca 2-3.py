#2.3 - Árvore de Busca 2-3

# Similar às árvores AVL, mas
# ▪ Cada nó interno tem dois ou três filhos;
# ▪ Pode ter uma ou duas chaves em cada nó;
# ▪ Todos os nós externos têm a mesma profundidade.

# – Quando o nó tem 1 chave: tem 2 filhos
# – Quando o nó tem 2 chaves: tem 3 filhos.


#Para implementar uma árvore 2-3, é necessário usar dois tipos
# de nós: um com uma chave e dois links e outro com duas chaves
# e três links.

